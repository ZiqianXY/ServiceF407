/**
  ******************************************************************************
  * @brief  串口USART1中断函数     
  * @param  None
  * @retval None
  ******************************************************************************
  */
void USART1_IRQHandler(void)                	//串口1中断服务程序
{
	u8 Receive;
    
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET) //接收中断(接收到的数据必须是0x0d 0x0a结尾)
    {     
		Receive =USART_ReceiveData(USART1);	//读取接收到的数据		

        if(USART_RX_STA&STA_RECEIVE_START)
        {      
            
            if((USART_RX_STA&STA_RECEIVE_END)==0)//接收未完成
            {    
                if(USART_RX_STA&STA_RECEIVE_END_ING)//上次接收到了0x0d
                {
                    if(Receive==REC_END){
                      USART_RX_STA|=STA_RECEIVE_END;	//接收完成了
                      send2usart3();
                    }
                    else 
                        USART_RX_STA=0;//接收错误,重新开始
                }
                else //上次没收到0X0D
                {
                    if(Receive==REC_END_ING){   //这次接收到了0x0d
                        USART_RX_STA|=STA_RECEIVE_END_ING;
                    }
                    else{   //这次仍然没接收到0x0d
                        USART_RX_STA++; //数据长度加1
                        lengthUsartBuffer=USART_RX_STA&0X0FFF;
                        USART_RX_BUF[lengthUsartBuffer-1]=Receive ; //保存数据至缓存区
                        if(lengthUsartBuffer==USART_REC_LEN){
                            USART_RX_STA=0;//接收数据错误,重新开始接收	  
                        }
                    }		 
                } 
            } 
        }else{
            if(USART_RX_STA&STA_RECEIVE_START_ING) { //上次收到了准备开始标记
               //printf("4");
                if(Receive==REC_START)  // 接受到开始标记
                    USART_RX_STA|=STA_RECEIVE_START;    //开始标记位置位
                else    //没收到开始标记，出错重置
                    USART_RX_STA=0;
                
            }else { //上次没收到准备开始标记

                if(Receive==REC_START_ING)  //这次收到准备开始标记
                    USART_RX_STA|=STA_RECEIVE_START_ING;    //准备开始标记位置位
            }
        }    
    }        
} 




